CREATE TABLE IF NOT EXISTS movimientos_inventario (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    inventario_id       INTEGER NOT NULL,
    tipo                VARCHAR(20) NOT NULL,
    cantidad            INTEGER NOT NULL,
    stock_anterior      INTEGER NOT NULL,
    stock_nuevo         INTEGER NOT NULL,
    costo_unitario      NUMERIC(10, 2),
    motivo              VARCHAR(250),
    referencia          VARCHAR(120),
    usuario_id          INTEGER,
    fecha_movimiento    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_movimientos_inventario_item
        FOREIGN KEY (inventario_id) REFERENCES inventario (id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT fk_movimientos_inventario_usuario
        FOREIGN KEY (usuario_id) REFERENCES usuarios (id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,
    CONSTRAINT ck_movimientos_inventario_tipo
        CHECK (tipo IN ('entrada', 'salida', 'ajuste', 'merma')),
    CONSTRAINT ck_movimientos_inventario_cantidad
        CHECK (cantidad >= 0),
    CONSTRAINT ck_movimientos_inventario_stock
        CHECK (stock_anterior >= 0 AND stock_nuevo >= 0)
);

CREATE INDEX IF NOT EXISTS idx_movimientos_inventario_item ON movimientos_inventario (inventario_id);
CREATE INDEX IF NOT EXISTS idx_movimientos_inventario_fecha ON movimientos_inventario (fecha_movimiento);
CREATE INDEX IF NOT EXISTS idx_movimientos_inventario_tipo ON movimientos_inventario (tipo);

