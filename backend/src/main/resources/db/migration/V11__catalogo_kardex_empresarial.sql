CREATE TABLE IF NOT EXISTS producto_receta (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    producto_id         INTEGER NOT NULL,
    inventario_id       INTEGER NOT NULL,
    cantidad_insumo     NUMERIC(10, 3) NOT NULL,
    unidad              VARCHAR(30) NOT NULL,
    activo              BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_producto_receta_producto
        FOREIGN KEY (producto_id) REFERENCES productos (id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT fk_producto_receta_inventario
        FOREIGN KEY (inventario_id) REFERENCES inventario (id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT ck_producto_receta_cantidad
        CHECK (cantidad_insumo > 0),
    CONSTRAINT ux_producto_receta_linea
        UNIQUE (producto_id, inventario_id)
);

CREATE INDEX IF NOT EXISTS idx_producto_receta_producto ON producto_receta (producto_id);
CREATE INDEX IF NOT EXISTS idx_producto_receta_inventario ON producto_receta (inventario_id);

CREATE TABLE IF NOT EXISTS lotes_inventario (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    inventario_id       INTEGER NOT NULL,
    codigo_lote         VARCHAR(80) NOT NULL,
    cantidad            INTEGER NOT NULL DEFAULT 0,
    fecha_vencimiento   DATE,
    fecha_ingreso       TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    costo_unitario      NUMERIC(10, 2),
    activo              BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_lotes_inventario_item
        FOREIGN KEY (inventario_id) REFERENCES inventario (id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT ck_lotes_inventario_cantidad
        CHECK (cantidad >= 0),
    CONSTRAINT ux_lotes_inventario_codigo
        UNIQUE (inventario_id, codigo_lote)
);

CREATE INDEX IF NOT EXISTS idx_lotes_inventario_item ON lotes_inventario (inventario_id);
CREATE INDEX IF NOT EXISTS idx_lotes_inventario_vencimiento ON lotes_inventario (fecha_vencimiento);

COMMENT ON TABLE movimientos_inventario IS 'Kardex de entradas, salidas, ajustes y mermas de inventario';
COMMENT ON TABLE producto_receta IS 'Relacion entre productos vendibles e insumos consumidos';
COMMENT ON TABLE lotes_inventario IS 'Control opcional de lotes y vencimientos por insumo';
